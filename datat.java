class datatypes {
    public static void main(String[] args) {
        int myNum = 5;
        float myFloatNum = 5.99f;
        char myLetter = 'D';
        boolean myBool = true;
        String myText = "Hello";
        System.out.print("Integer: " + myNum + "\nFloat: " + myFloatNum + "\nCharacter: " + myLetter);
        System.out.println("\nString: " + myText + "\nBoolean: " + myBool);
    }
}